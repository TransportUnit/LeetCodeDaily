    [ResultGenerator]
    public int CountTriples(int n)
    {
        int left = 0;
        int right = _THRESHOLDS.Length - 1;

        while (left <= right)
        {
            int mid = left + (right - left) / 2;
            if (_THRESHOLDS[mid] == n)
            {
                return _VALUES[mid];
            }
            if (_THRESHOLDS[mid] > n)
            {
                right = mid - 1;
            }
            else
            {
                left = mid + 1;
            }
        }

        return _VALUES[right];
    }
}